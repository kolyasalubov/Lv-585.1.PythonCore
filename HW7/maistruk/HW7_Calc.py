import math

def add_numbers():
    a = float(input("Enter first number: "))
    b = float(input("Enter second number: "))
    return a + b
def sub_numbers():
    m = float(input("Enter number: "))
    n = float(input("Enter second number: "))
    return m - n
def mul_numbers():
    c = float(input("Enter first number: "))
    k = float(input("Enter second number: "))
    return c * k
def div_numbers():
    l = float(input("Enter first number: "))
    p = float(input("Enter second number: "))
    return l / p
