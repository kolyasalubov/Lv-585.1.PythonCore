def addition(a, b):
    """
    This function returns the result of addition.
    """

    return a + b

def subtraction(a, b):
    """
    This function returns the result of subtraction.
    """

    return a - b

def multiplication(a, b):
    """
    This function returns the result of multiplication.
    """

    return a * b

def division(a, b):
    """
    This function returns the result of division.
    """

    return a / b
